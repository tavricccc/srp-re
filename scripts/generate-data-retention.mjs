import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const configPath = path.join(projectRoot, 'config', 'data-retention.config.json');

try {
  const raw = JSON.parse(await readFile(configPath, 'utf8'));
  const config = Object.fromEntries(Object.entries(raw).map(([key, value]) => {
    if (typeof value !== 'number' || !Number.isInteger(value) || value <= 0) {
      throw new Error(`${key} 必須是正整數。`);
    }
    return [key, value];
  }));
  const rendered = `// This file is generated automatically by scripts/generate-data-retention.mjs.\n// Do not edit this file manually.\n\nexport const DATA_RETENTION = ${JSON.stringify(config, null, 2)} as const;\n`;
  const outputPaths = [
    path.join(projectRoot, 'src', 'generated', 'data-retention.ts'),
    path.join(projectRoot, 'supabase', 'functions', '_shared', 'data-retention.ts'),
  ];
  await Promise.all(outputPaths.map(async (outputPath) => {
    await mkdir(path.dirname(outputPath), { recursive: true });
    await writeFile(outputPath, rendered, 'utf8');
  }));
  console.info('Generated data retention configuration.');
} catch (error) {
  console.error('Failed to generate data retention config:', error instanceof Error ? error.message : error);
  process.exit(1);
}
